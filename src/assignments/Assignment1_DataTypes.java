package assignments;

public class Assignment1_DataTypes {
	    public static void main(String[] args) {
	        byte b = 100;
	        short s = 30000;
	        int i = 2000000000;
	        long l = 9223372036854775807L;
	        float f = 5.75f;
	        double d = 19.99;
	        char c = 'A';
	        boolean isJavaFun = true;
	        String greeting = "Hello";

	        System.out.println("Byte value: " + b);
	        System.out.println("Short value: " + s);
	        System.out.println("Integer value: " + i);
	        System.out.println("Long value: " + l);
	        System.out.println("Float value: " + f);
	        System.out.println("Double value: " + d);
	        System.out.println("Character value: " + c);
	        System.out.println("Boolean value: " + isJavaFun);
	        System.out.println("String value: " + greeting);

	        // Operations
	        int a = 5, b2 = 10;
	        System.out.println("Sum of integers: " + (a + b2));

	        String name = " Mahen";
	        System.out.println("Greeting: " + greeting + name);

	        double intToDouble = i;
	        System.out.println("Int to Double: " + intToDouble);
	    }
	}


